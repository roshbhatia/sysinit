local wezterm = require("wezterm")

local keybindings = require("sysinit.pkg.keybindings")
local utils = require("sysinit.pkg.utils")
local plugin_loader = require("sysinit.pkg.plugin_loader")

local M = {}

function M.setup(config)
  local config_data = utils.load_json_file(utils.get_config_path("config.json"))
  local font = wezterm.font_with_fallback({
    {
      family = config_data.font.monospace,
      harfbuzz_features = { "calt", "liga" },
    },
    config_data.font.symbols,
  })

  -- Core settings
  config.font = font
  config.font_size = 11.0
  config.line_height = 1.0
  config.cell_width = 1.0

  -- Apply base16 colors from config.json (generated by Nix from Stylix)
  if config_data.colors then
    config.colors = config_data.colors
  end

  config.status_update_interval = 150
  config.animation_fps = 240
  config.max_fps = 240
  config.cursor_blink_rate = 320
  config.cursor_thickness = 1
  config.scrollback_lines = 200000
  config.quick_select_alphabet = "fjdkslaghrueiwoncmv"
  config.adjust_window_size_when_changing_font_size = false
  config.use_resize_increments = false
  config.pane_focus_follows_mouse = false

  config.tab_bar_at_bottom = true
  config.use_fancy_tab_bar = false
  config.hide_tab_bar_if_only_one_tab = false

  -- Every fresh window (launch, relaunch after quit, CMD+N) starts in the
  -- "default" workspace instead of inheriting the last-used session. Paired
  -- with wm.session_restore_on_startup = false so nothing overrides this on
  -- startup.
  config.default_workspace = "default"

  config.window_frame = {
    font = font,
    font_size = 11.0,
  }

  config.visual_bell = {
    fade_in_function = "EaseIn",
    fade_in_duration_ms = 70,
    fade_out_function = "EaseOut",
    fade_out_duration_ms = 100,
  }

  config.window_background_opacity = config_data.transparency.opacity

  if utils.is_darwin() then
    -- macOS: compositor handles blur, wezterm handles opacity
    config.front_end = "WebGpu"
    config.window_decorations = "RESIZE|MACOS_FORCE_ENABLE_SHADOW"
    config.macos_window_background_blur = config_data.transparency.blur
  else
    -- Linux/Wayland (sway)
    config.enable_wayland = false
    config.front_end = "WebGpu"
    config.window_decorations = "RESIZE"
    config.freetype_load_flags = "NO_HINTING|NO_AUTOHINT"
    -- Tell wezterm it's in a tiling WM so it accounts for
    -- window sizing correctly (fixes tab bar cutoff)
    config.tiling_desktop_environments = {
      "X11 LG3D",
      "X11 bspwm",
      "X11 i3",
      "X11 dwm",
      "Wayland",
    }
  end

  local function locked_indicator()
    if keybindings.locked_mode then
      return "  "
    end
    return "  "
  end

  -- Load agent-deck before tabline so its API is available in tabline sections
  local agent_deck_ok, agent_deck = plugin_loader.load("agent-deck")
  if not agent_deck_ok then
    wezterm.log_warn("Failed to load agent-deck: " .. tostring(agent_deck))
  end
  if agent_deck_ok then
    agent_deck.apply_to_config(config, {
      tab_title = { enabled = false },
      right_status = { enabled = false },
      cooldown_ms = 150,
      max_lines = 500,
      notifications = {
        enabled = true,
        on_waiting = true,
        backend = "native",
      },
      agents = {
        claude = {
          -- Claude Code 2.x ships as a Bun-compiled Mach-O binary whose
          -- kernel-visible name is `.claude-wrapped`; the broad "claude"
          -- substring matches the install path .../claude-X.Y.Z/bin/.claude-wrapped.
          patterns = { ".claude%-wrapped", "claude", "claude%-code" },
          executable_patterns = { "@anthropic%-ai/claude%-code", "/claude%-code/", "/claude$", "claude" },
          argv_patterns = { "@anthropic%-ai/claude%-code", "claude%-code", "^claude%s*$" },
          title_patterns = { "claude code", "claude", ".claude%-wrapped" },
        },
        goose = {
          patterns = { "goose", "goosed" },
          executable_patterns = { "/goose$", "/goosed$" },
          argv_patterns = { "^goose%s*$" },
          title_patterns = { "goose" },
        },
        amp = {
          patterns = { "amp" },
          executable_patterns = { "/amp$" },
          argv_patterns = { "^amp%s*$" },
          title_patterns = { "amp" },
        },
        copilot = {
          patterns = { "copilot" },
          executable_patterns = { "/copilot$", "copilot%-language%-server" },
          argv_patterns = { "^copilot%s*$" },
          title_patterns = { "copilot" },
        },
        cursor = {
          patterns = { "cursor%-agent", "cursor" },
          executable_patterns = { "/cursor%-agent$" },
          argv_patterns = { "cursor%-agent" },
          title_patterns = { "cursor" },
        },
        crush = {
          patterns = { "crush" },
          executable_patterns = { "/crush$" },
          argv_patterns = { "^crush%s*$" },
          title_patterns = { "crush" },
        },
        aider = {
          patterns = { "aider" },
          executable_patterns = { "/aider$" },
          argv_patterns = { "^aider%s*$" },
          title_patterns = { "aider" },
        },
        opencode = {
          patterns = { "opencode" },
          executable_patterns = { "opencode%-darwin", "opencode%-linux", "/opencode$" },
          argv_patterns = { "bunx%s+opencode", "npx%s+opencode", "/opencode$" },
          title_patterns = { "opencode" },
        },
      },
    })

    -- Prune stale agent-deck state for closed panes (fixes count-never-decrements bug).
    -- agent-deck's own update-status handler only visits panes that currently exist;
    -- when a pane is closed its entry stays in state.agent_states forever and the count
    -- never decrements. get_all_agent_states() returns the live table by reference so
    -- we can mutate it. This handler runs after agent-deck's (registered first above).
    wezterm.on("update-status", function(window, _pane)
      local all_states = agent_deck.get_all_agent_states()
      local active_ids = {}
      for _, tab in ipairs(window:mux_window():tabs()) do
        for _, p in ipairs(tab:panes()) do
          active_ids[p:pane_id()] = true
        end
      end
      for pane_id in pairs(all_states) do
        if not active_ids[pane_id] then
          all_states[pane_id] = nil
        end
      end
    end)
  end

  -- Status labels so the icons are readable:
  --   ● N working  = agent actively processing (running a tool, generating)
  --   ◔ N waiting  = agent needs your input (permission prompt, y/n)
  --   ○ N idle     = agent at prompt, ready for next message
  local agent_status_labels = {
    working = "working",
    waiting = "waiting",
    idle = "idle",
  }

  local function agent_status()
    if not agent_deck_ok then
      return ""
    end
    local counts = agent_deck.count_agents_by_status()
    local parts = {}
    for _, s in ipairs({ "working", "waiting", "idle" }) do
      if counts[s] and counts[s] > 0 then
        table.insert(parts, {
          Text = " " .. agent_deck.get_status_icon(s) .. " " .. counts[s] .. " " .. agent_status_labels[s] .. " ",
        })
      end
    end
    if #parts == 0 then
      return ""
    end
    return wezterm.format(parts)
  end

  local tabline_ok, tabline = plugin_loader.load("tabline")
  if not tabline_ok then
    wezterm.log_warn("Failed to load tabline.wez: " .. tostring(tabline))
  end
  if tabline_ok then
    tabline.setup({
      options = {
        theme = config.colors,
        tabs_enabled = false,
        section_separators = {
          left = "",
          right = "",
        },
        component_separators = {
          left = "",
          right = "",
        },
        tab_separators = {
          left = " |",
          right = "| ",
        },
      },
      sections = {
        tabline_a = {
          "mode",
          locked_indicator,
        },
        tabline_b = {},
        tabline_x = {},
        tabline_y = { agent_status },
        tabline_z = {
          "workspace",
          "domain",
        },
      },
      extensions = {},
    })
    tabline.apply_to_config(config)
  end

  -- Fallback: if tabline plugin didn't load, use native right_status for locked indicator
  if not tabline_ok then
    wezterm.on("update-right-status", function(window, _pane)
      local mode_text = locked_indicator()
      window:set_right_status(wezterm.format({
        { Text = mode_text },
      }))
    end)
  end

  -- Post-process window padding for macOS
  if utils.is_darwin() then
    -- Set window padding AFTER tabline.apply_to_config, which zeroes all padding
    config.window_padding = {
      left = "1cell",
      right = "1cell",
      top = "1cell",
      bottom = "0cell",
    }
  end

  -- sravioli UI toolkit: warp (stdlib) must load first so ribbon/sigil's
  -- deps.lua `require("warp.api")` resolves off package.path. These bypass
  -- wezterm.plugin.require entirely (see plugin_loader).
  plugin_loader.load("warp")
  local ribbon_ok, ribbon = plugin_loader.load("ribbon")
  if not ribbon_ok then
    wezterm.log_warn("Failed to load ribbon.wz: " .. tostring(ribbon))
  end
  local sigil_ok, sigil = plugin_loader.load("sigil")
  if not sigil_ok then
    wezterm.log_warn("Failed to load sigil.wz: " .. tostring(sigil))
  end
  if sigil_ok then
    sigil.setup({
      -- Claude Code's foreground process basename is `.claude-wrapped` (a Bun
      -- Mach-O binary); register it (and aliases) so tabs show a clean glyph
      -- instead of the raw wrapper name.
      overrides = {
        claude = {
          name = "Claude",
          -- Claude's brand sparkle (U+2733), in the brand orange. Marks every
          -- Claude Code tab consistently instead of the raw OSC spinner glyph.
          icon = "✳",
          color = "#D97757",
          aliases = { ".claude-wrapped", "claude-code", "claude-wrapped" },
        },
      },
    })
  end

  -- lantern: runtime appearance picker (colorschemes, fonts, GPU, opacity, …).
  -- Its bundled "flames" (the choice sets) live at <plugin_dir>/plugin/lantern/
  -- flames; lantern.meta locates that dir from either a plugin url containing
  -- "lantern.wz" OR wezterm.GLOBAL.__lantern_plugin_dir. Our Nix-store paths
  -- (/nix/store/HASH-source) carry neither, so we seed the global explicitly
  -- BEFORE loading — otherwise register_builtins() (run at api.lua load) wires
  -- the colorscheme/font/gpu wicks with no choices. Its deps.lua (log/memo/
  -- ribbon/warp) is patched to resolve through plugin_loader instead of
  -- wezterm.plugin.require (see patches/lantern-deps-loader.patch).
  wezterm.GLOBAL = wezterm.GLOBAL or {}
  wezterm.GLOBAL.__lantern_plugin_dir = config_data.plugins and config_data.plugins.lantern
  local lantern_ok, lantern = plugin_loader.load("lantern")
  if not lantern_ok then
    wezterm.log_warn("Failed to load lantern.wz: " .. tostring(lantern))
  end
  if lantern_ok then
    lantern.setup({
      log = { enabled = false },
      -- Baseline the picker's "reset" font size to ours so it round-trips.
      default_font = { font_size = config.font_size },
      color = { opacity = config_data.transparency.opacity },
    })
    -- Restore the appearance the user picked in a previous session.
    lantern.rekindle(config)

    -- One entry point (SUPER+SHIFT+l) instead of the upstream CTRL+SHIFT+c/f/…
    -- bindings, which collide with our copy (CTRL+SHIFT+c) and pane-select
    -- (CTRL+SHIFT+f). Picking a category opens that wick's own selector —
    -- lantern.light.<x>() returns a ready WezTerm action.
    local lantern_categories = {
      { id = "colorscheme", label = "Colorscheme", fn = lantern.light.colorscheme },
      { id = "font", label = "Font", fn = lantern.light.font },
      { id = "font_size", label = "Font size", fn = lantern.light.font_size },
      { id = "font_leading", label = "Font leading", fn = lantern.light.font_leading },
      { id = "font_ligatures", label = "Font ligatures", fn = lantern.light.font_ligatures },
      { id = "gpu", label = "GPU / front end", fn = lantern.light.gpu },
      { id = "window_opacity", label = "Window opacity", fn = lantern.light.window_opacity },
      { id = "window_padding", label = "Window padding", fn = lantern.light.window_padding },
      { id = "inactive_pane_opacity", label = "Inactive pane opacity", fn = lantern.light.inactive_pane_opacity },
      { id = "cursor_style", label = "Cursor style", fn = lantern.light.cursor_style },
      { id = "tab_bar_style", label = "Tab bar style", fn = lantern.light.tab_bar_style },
    }

    config.keys = config.keys or {}
    table.insert(config.keys, {
      key = "l",
      mods = "SUPER|SHIFT",
      action = wezterm.action_callback(function(win, pane)
        if keybindings.locked_mode then
          win:perform_action({ SendKey = { key = "l", mods = "SUPER|SHIFT" } }, pane)
          return
        end
        local choices = {}
        for _, cat in ipairs(lantern_categories) do
          choices[#choices + 1] = { id = cat.id, label = cat.label }
        end
        win:perform_action(
          wezterm.action.InputSelector({
            title = "Appearance",
            fuzzy = true,
            choices = choices,
            action = wezterm.action_callback(function(inner_win, inner_pane, id, _label)
              if not id then
                return
              end
              for _, cat in ipairs(lantern_categories) do
                if cat.id == id then
                  inner_win:perform_action(cat.fn(), inner_pane)
                  return
                end
              end
            end),
          }),
          pane
        )
      end),
    })
  end

  -- Smart tab titles: a sigil process icon + a meaningful label, never empty
  -- and never the bare word "zsh". Label precedence is OSC-2 title (oh-my-posh
  -- emits the folder for shells; TUIs like Claude emit their own summary) ->
  -- cwd basename -> process name. No app-specific slugging: programs that set
  -- an OSC title own their label verbatim.
  local SHELLS = { zsh = true, bash = true, fish = true, sh = true, ["-zsh"] = true }

  wezterm.on("format-tab-title", function(tab, _tabs, _panes, _config, _hover, _max_width)
    -- format-tab-title hands us TabInformation/PaneInformation structs (fields),
    -- NOT MuxTab/Pane objects (methods). Calling methods here throws and aborts
    -- the handler, dropping WezTerm back to its default tab text.
    local pane = tab.active_pane
    local explicit = tab.tab_title -- set via tab:set_title(), else ""
    local osc = pane and pane.title -- the pane's OSC-2 title
    local proc = pane and pane.foreground_process_name
    if proc then
      proc = proc:match("([^/]+)$")
    end

    -- A user- (or program-) set, non-numeric tab title always wins.
    if explicit and explicit ~= "" and not explicit:match("^%d+$") then
      return explicit
    end

    -- cwd basename, collapsing $HOME to ~.
    local dir
    local cwd_uri = pane and pane.current_working_dir
    if cwd_uri then
      local cwd = cwd_uri.file_path or tostring(cwd_uri)
      local home = os.getenv("HOME") or ""
      if home ~= "" and cwd == home then
        dir = "~"
      else
        dir = cwd:match("([^/]+)/?$") or cwd
      end
    end

    -- Pick the label. Prefer the OSC-2 title when it carries real info, but
    -- WezTerm defaults pane.title to the bare process name ("zsh") when nothing
    -- sets an OSC title -- skip that and fall back to the cwd folder so a shell
    -- pane shows its directory rather than the word "zsh".
    local label
    if osc and osc ~= "" and not osc:match("^%d+$") and not SHELLS[osc:lower()] then
      label = osc
    end
    label = label or dir or proc or "shell"

    -- Prefix a sigil process icon when one is known (reset=true so the icon's
    -- color doesn't bleed into the label). Unknown procs (fallback=false)
    -- render the label alone.
    if proc and sigil_ok and ribbon_ok then
      local icon_items = sigil.items(proc, { padding = "right", fallback = false, reset = true })
      if icon_items and #icon_items > 0 then
        local r = ribbon.new("tab")
        r:append_items(icon_items)
        r:append(nil, nil, label)
        return r:items()
      end
    end
    return label
  end)

  -- Hyperlink rules: start with defaults, then add custom patterns
  local hyperlink_rules = wezterm.default_hyperlink_rules()

  -- Nix store paths as file:// links
  table.insert(hyperlink_rules, {
    regex = [[(/nix/store/[a-z0-9]{32}[a-zA-Z0-9._+%-][^\s]*)]],
    format = "file://$1",
  })

  -- owner/repo#NNN → GitHub issue URL
  table.insert(hyperlink_rules, {
    regex = [[([a-zA-Z0-9_.-]+/[a-zA-Z0-9_.-]+)#([0-9]+)]],
    format = "https://github.com/$1/issues/$2",
  })

  config.hyperlink_rules = hyperlink_rules

  -- Extended quick-select patterns
  config.quick_select_patterns = {
    "/nix/store/[a-z0-9]{32}[a-zA-Z0-9._+%-][^\\s]*",
    "[0-9a-f]{40}",
    "(?<![0-9a-f])[0-9a-f]{7}(?![0-9a-f])",
    "[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}",
    "/[^\\s]+",
  }

  -- workspace-manager: seshy session picker + layout persistence across restarts.
  -- We feed its switcher from `sy list`, bind it to SUPER+s (see below), and also
  -- surface it in the command palette. We do NOT use the plugin's own
  -- apply_to_config keybindings (they'd shadow vim's CTRL+[).
  local wm_ok, wm = plugin_loader.load("workspace-manager")
  if not wm_ok then
    wezterm.log_warn("Failed to load workspace-manager.wezterm: " .. tostring(wm))
  end
  if wm_ok then
    local home = os.getenv("HOME") or ""
    local seshy_dir = home .. "/.local/state/seshy/sessions"

    -- wezterm.run_child_process runs under the GUI process's own environment,
    -- whose PATH (when launched from Finder/Dock on macOS) omits ~/.local/bin
    -- where `sy` lives — so a bare "sy" silently fails and the switcher shows
    -- no sessions. Resolve an absolute path from the PATH the config knows
    -- about (env.json), falling back to the conventional install dir.
    local sy_bin = home .. "/.local/bin/sy"
    do
      local env = utils.load_json_file(utils.get_config_path("env.json"))
      for dir in (env and env.PATH or ""):gmatch("[^:]+") do
        local candidate = dir .. "/sy"
        local fh = io.open(candidate, "r")
        if fh then
          fh:close()
          sy_bin = candidate
          break
        end
      end
    end

    -- Replace the default choice provider: list seshy sessions instead of
    -- scanning a projects dir. `sy list` prints a header row then one row per
    -- session; column 1 is the session name.
    --
    -- The list always leads with a pinned "default" entry — the home-base
    -- workspace WezTerm starts in. Selecting it does SwitchToWorkspace{name=
    -- "default"} (create-if-absent), the one switcher path back from any seshy
    -- session. It's seeded first so it survives an empty/header-only `sy list`
    -- or a missing/erroring `sy` binary (the early return still carries it).
    wm.get_choices = function()
      local choices = {
        { name = "default", path = home, label = "default" },
      }
      local ok, stdout = pcall(function()
        local success, out = wezterm.run_child_process({ sy_bin, "list" })
        if not success then
          error("sy list failed")
        end
        return out
      end)
      if not ok or not stdout then
        return choices
      end
      local first = true
      for _, line in ipairs(wezterm.split_by_newlines(stdout)) do
        if first then
          first = false
        elseif line ~= "" then
          local name = line:match("^(%S+)")
          if name then
            table.insert(choices, {
              name = name,
              path = seshy_dir .. "/" .. name,
              label = name,
            })
          end
        end
      end
      return choices
    end

    wm.session_enabled = true
    -- Always launch (and relaunch after quit) into the "default" workspace
    -- rather than auto-restoring the most-recently-used session. With this
    -- false the plugin registers no gui-startup restore handler, so WezTerm's
    -- own startup honors config.default_workspace ("default") below. Sessions
    -- still save (session_enabled) and still restore when switched into via the
    -- switcher; only the startup auto-restore is suppressed.
    wm.session_restore_on_startup = false
    wm.session_state_dir = home .. "/.local/state/wezterm/workspace_state"
    wm.workspace_switcher_sort = "recency"

    -- apply_to_config appends its own leader bindings and an in-switcher
    -- key_table (config.key_tables.workspace_switcher_actions). We keep the
    -- key_table — it drives the switcher's own UI — but strip the four
    -- user-facing leader bindings it injects: we open the switcher from SUPER+s
    -- below, and CTRL+[ (= ESC) in particular would shadow vim. Remove by
    -- (key, mods) identity so the override is explicit and immune to any
    -- change in the plugin's append order. None of these combos are bound by
    -- our own keybindings.lua, so this only removes the plugin's.
    wm.apply_to_config(config)
    local wm_injected_keys = {
      { key = "s", mods = "LEADER" },
      { key = "S", mods = "LEADER" },
      { key = "]", mods = "CTRL" },
      { key = "[", mods = "CTRL" },
    }
    if config.keys then
      for _, injected in ipairs(wm_injected_keys) do
        for i = #config.keys, 1, -1 do
          local k = config.keys[i]
          if k.key == injected.key and k.mods == injected.mods then
            table.remove(config.keys, i)
          end
        end
      end
    end

    -- SUPER+s opens the switcher. Bound here (not keybindings.lua) because it
    -- needs this configured wm instance; mirrors the locked-mode passthrough the
    -- other smart keybinds use. ssh moved to SUPER+SHIFT+s.
    config.keys = config.keys or {}
    table.insert(config.keys, {
      key = "s",
      mods = "SUPER",
      action = wezterm.action_callback(function(win, pane)
        if keybindings.locked_mode then
          win:perform_action({ SendKey = { key = "s", mods = "SUPER" } }, pane)
          return
        end
        win:perform_action(wm.workspace_switcher(), pane)
      end),
    })

    -- Also expose the switcher through the command palette (reachable via the
    -- existing SUPER+: / SUPER+; / CTRL+; palette bindings).
    wezterm.on("augment-command-palette", function(_window, _pane)
      return {
        {
          brief = "Switch seshy session / workspace",
          action = wm.workspace_switcher(),
        },
        {
          brief = "Switch to previous workspace",
          action = wm.switch_to_previous_workspace(),
        },
      }
    end)
  end
end

return M
